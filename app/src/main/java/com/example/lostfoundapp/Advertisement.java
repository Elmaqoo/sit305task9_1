package com.example.lostfoundapp;

public class Advertisement {
    private long id;
    private String type;
    private String name;
    private String phone;
    private String description;
    private String date;
    private String location; // Store latitude and longitude as "lat,lng"
    private String placeName; // Store the place name

    // Constructor for new Advertisement (no ID yet)
    public Advertisement(String type, String name, String phone, String description, String date, String location, String placeName) {
        this.type = type;
        this.name = name;
        this.phone = phone;
        this.description = description;
        this.date = date;
        this.location = location;
        this.placeName = placeName;
    }

    // Constructor including ID (from database)
    public Advertisement(long id, String type, String name, String phone, String description, String date, String location, String placeName) {
        this.id = id;
        this.type = type;
        this.name = name;
        this.phone = phone;
        this.description = description;
        this.date = date;
        this.location = location;
        this.placeName = placeName;
    }

    // Getters and Setters
    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getPlaceName() {
        return placeName;
    }

    public void setPlaceName(String placeName) {
        this.placeName = placeName;
    }
}
